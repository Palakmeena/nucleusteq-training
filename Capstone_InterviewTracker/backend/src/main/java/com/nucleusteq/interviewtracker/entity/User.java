package com.nucleusteq.interviewtracker.entity;

import com.nucleusteq.interviewtracker.enums.UserRole;
import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.EnumType;
import jakarta.persistence.Enumerated;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.Table;
import java.time.LocalDateTime;

/**
 * Represents a user in the system.
 * Base entity for authentication and authorization.
 * All three roles — HR, PANEL and CANDIDATE — have a User account.
 * Panel accounts start inactive until they set their password via email link.
 */
@Entity
@Table(name = "users")
public class User {

    /** Primary key — auto generated by database. */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    /** Full name of the user. */
    @Column(name = "full_name", nullable = false)
    private String fullName;

    /** Email address used as the login username — must be unique. */
    @Column(name = "email", nullable = false, unique = true)
    private String email;

    /** Encrypted password — never stored as plain text. */
    @Column(name = "password", nullable = false)
    private String password;

    /** Role of the user — determines what they can access in the system. */
    @Enumerated(EnumType.STRING)
    @Column(name = "role", nullable = false)
    private UserRole role;

    /** Whether this user account is active.
     *  Panel members start as inactive until they set their password. */
    @Column(name = "is_active", nullable = false)
    private boolean isActive = false;

    /** Secure token sent to panel member via email to set their password.
     *  Null for HR and Candidate users. */
    @Column(name = "activation_token")
    private String activationToken;

    /** Expiry time of the activation token — valid for 24 hours. */
    @Column(name = "token_expiry")
    private LocalDateTime tokenExpiry;

    /** Timestamp when this user account was created. */
    @Column(name = "created_at", nullable = false)
    private LocalDateTime createdAt;

    /**
     * Default constructor required by JPA.
     */
    public User() {
    }

    /**
     * Creates a new user with all required fields.
     */
    public User(String fullName, String email, String password, UserRole role) {
        this.fullName = fullName;
        this.email = email;
        this.password = password;
        this.role = role;
        this.isActive = false;
        this.createdAt = LocalDateTime.now();
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public UserRole getRole() {
        return role;
    }

    public void setRole(UserRole role) {
        this.role = role;
    }

    public boolean isActive() {
        return isActive;
    }

    public void setActive(boolean active) {
        isActive = active;
    }

    public String getActivationToken() {
        return activationToken;
    }

    public void setActivationToken(String activationToken) {
        this.activationToken = activationToken;
    }

    public LocalDateTime getTokenExpiry() {
        return tokenExpiry;
    }

    public void setTokenExpiry(LocalDateTime tokenExpiry) {
        this.tokenExpiry = tokenExpiry;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }
}
"""
Q38. Explain the difference between a module and a package.
"""


def explain_difference() -> None:
    """
    Displays the difference between a module and a package.
    """

    print("MODULE")
    print("- A module is a single Python file.")
    print("- It contains functions, classes, and variables.")
    print("- Example: q37_utility_module.py")

    print("\nPACKAGE")
    print("- A package is a collection of related modules.")
    print("- A package contains an __init__.py file.")
    print("- Example: q40_math_package")


if __name__ == "__main__":
    explain_difference()